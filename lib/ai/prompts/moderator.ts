export const SYNTHESIS_SYSTEM_PROMPT = `تو سنتزکننده نهایی یک شورای هوش مصنوعی هستی. چند مدل پاسخ داده‌اند و یک نقد مقایسه‌ای هم آماده شده.
وظیفه تو: قوی‌ترین پاسخ نهایی ممکن را بنویس.
قواعد:
- بهترین بخش‌های هر پاسخ را ترکیب کن و خطاهایی که در نقد مشخص شده را کنار بگذار.
- فارسی، طبیعی و خوانا بنویس؛ از تیتر مارک‌داون (##) و عنوان‌های ساختگی استفاده نکن.
- پاسخ باید مستقل و کامل باشد؛ به «مدل ۱ گفت…» ارجاع نده.
- اگر بین مدل‌ها اختلاف مهمی بود که قابل حل نیست، صادقانه هر دو دیدگاه را با ذکر عدم قطعیت بیاور.`;

export function buildCritiqueUserMessage(
  question: string,
  answers: Array<{ label: string; text: string }>
): string {
  const parts = [`سؤال کاربر:\n${question}`, ""];
  for (const a of answers) {
    parts.push(`--- پاسخ ${a.label} ---`, a.text, "");
  }
  parts.push("حالا اختلاف‌ها و نقاط قوت/ضعف را تحلیل کن.");
  return parts.join("\n");
}

export function buildSynthesisUserMessage(
  question: string,
  answers: Array<{ label: string; text: string }>,
  critique: string
): string {
  const parts = [`سؤال کاربر:\n${question}`, ""];
  for (const a of answers) {
    parts.push(`--- پاسخ ${a.label} ---`, a.text, "");
  }
  parts.push("--- نقد ناظر ---", critique, "", "حالا پاسخ نهایی و قوی را بنویس.");
  return parts.join("\n");
}
