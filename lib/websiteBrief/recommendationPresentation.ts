export function getRecommendationPresentation(
  service: RecommendedService,
  reason: string,
  serviceLabel: string
): RecommendationPresentation {
  if (service === "none") {
    return {
      ...DEFAULT,
      badge: "ثبت موفق",
      headline: "پیشنهاد آرایه روی طراحی سایت متمرکز است",
      reason,
      benefits: [
        "تیم آرایه اطلاعات شما را بررسی می‌کند",
        "پیشنهاد قیمت متناسب با اهداف و بخش‌های انتخاب‌شده آماده می‌شود",
        "در صورت نیاز، در تماس اول جزئیات تکمیل می‌شود",
      ],
      fomo: "",
      gain: "",
      ctaPrimary: "",
      ctaSecondary: "",
      acceptedMessage: "",
      declinedMessage: "",
    };
  }

  const preset = BY_SERVICE[service];
  return {
    ...preset,
    reason,
    headline: preset.headline || serviceLabel,
  };
}
