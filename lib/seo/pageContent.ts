export function getIndustryLandingPageContent(serviceType: ServiceType, slug: IndustrySlug): IndustryLandingPageContent {
  const persianIndustryName = getPersianIndustryName(slug);

  const canonicalPath = `/${serviceType}/${slug}`;

  if (serviceType === "seo") {
    const hero = seoHeroByIndustry[slug];
    const base = baseProblemByIndustry[slug];
    return {
      serviceType,
      slug,
      persianIndustryName,
      hero: {
        h1: hero.h1,
        lead: hero.lead,
        primaryCtaLabel: commonPrimaryCtaLabel,
        secondaryCtaLabel: commonSecondaryCtaLabel,
      },
      problem: {
        title: seoProblemTitleByIndustry[slug],
        description: base.description,
        impact: problemImpactByIndustry[slug],
      },
      commonMistakes: base.commonMistakes,
      whatAraayeBuildsFixes: seoDeliverablesByIndustry[slug],
      whatYouReceive: seoWhatYouReceiveByIndustry[slug],
      mvpPlanWeeks: seoMvpPlanByIndustry[slug],
      requiredPagesAndFeatures: base.requiredPagesAndFeatures,
      whoFor: base.whoFor,
      notFor: base.notFor,
      faqs: seoFaqsByIndustry[slug],
      ctaBlock: {
        ...ctaBlock,
      },
      meta: {
        title: seoMetaByIndustry[slug].title,
        description: seoMetaByIndustry[slug].description,
        canonicalPath,
      },
    };
  }

  const hero = websiteHeroByIndustry[slug];
  const base = baseProblemByIndustry[slug];
  const isPrivateTutorWebsite = slug === "private-tutor";
  return {
    serviceType,
    slug,
    persianIndustryName,
    hero: {
      h1: hero.h1,
      lead: hero.lead,
      primaryCtaLabel: isPrivateTutorWebsite ? "مشاهده سایت مخصوص مدرس‌ها" : commonPrimaryCtaLabel,
      secondaryCtaLabel: commonSecondaryCtaLabel,
      ...(isPrivateTutorWebsite ? { primaryCtaHref: "/modares" } : {}),
    },
    problem: {
      title: base.title,
      description: base.description,
      impact: problemImpactByIndustry[slug],
    },
    commonMistakes: base.commonMistakes,
    whatAraayeBuildsFixes: websiteDeliverablesByIndustry[slug],
    whatYouReceive: websiteWhatYouReceiveByIndustry[slug],
    mvpPlanWeeks: websiteMvpPlanByIndustry[slug],
    requiredPagesAndFeatures: base.requiredPagesAndFeatures,
    whoFor: base.whoFor,
    notFor: base.notFor,
    faqs: websiteFaqsByIndustry[slug],
    ctaBlock: {
      ...ctaBlock,
    },
    meta: {
      title: websiteMetaByIndustry[slug].title,
      description: websiteMetaByIndustry[slug].description,
      canonicalPath,
    },
  };
}

export function getIndustryLandingPageContentsForAll(): IndustryLandingPageContent[] {
  const list: IndustryLandingPageContent[] = [];
  for (const slug of industries.map((i) => i.slug)) {
    list.push(getIndustryLandingPageContent("seo", slug));
    list.push(getIndustryLandingPageContent("website", slug));
  }
  return list;
}

